// filter method (since we apply filter on array and returns arrays, we can chain the filter methods on the arrays and returned filtered arrays)
//books with more than 500 pages
const longBooks = books
  .filter((book) => book.pages > 500)
  .filter((book) => book.hasMovieAdaptation);
longBooks;

// since the filter method returns an array, we could chain a map on the newly returned array
const adventureBooks = books
  .filter((books) => books.genres.includes("adventrue"))
  .map((book) => book.title);
adventureBooks;

//use case of reduced method (adding together numbers) :reduce method reduces the values of an array to one value
const pagesAllBooks = books.reduce((acc, book) => acc + book.pages, 0);
pagesAllBooks;

//sort method (used to sort an array) (it's not a functional array, it mutates the original array not good for react) solution first make a copy then chain the method
const arr = [3, 7, 1, 9, 6];
const sorted = arr.slice().sort((a, b) => a - b); //a and b are always the number and the next number and when use a (a-b) implys sorting ascending and (b-a) descending
sorted;
arr;

const sortedByPages = books.slice().sort((a, b) => a.pages - b.pages);
sortedByPages;

