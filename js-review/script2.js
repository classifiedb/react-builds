//The array map method (map, filter and reduce these methods do not mutate the original array, hence they create an new array)
const books = getBooks();

//Optional chaining operator
function getTotalReviewCount(book) {
  const goodreads = book.reviews.goodreads.reviewsCount;
  const librarything = book.reviews.librarything?.reviewsCount ?? 0; //use optional chaining to check whether we can proceed with the chaining if the first  elements exist
  librarything;
  return goodreads + librarything;
}

